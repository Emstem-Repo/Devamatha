package com.kp.cms.to.serviceLearning;

public class ServiceLearningMarksEntryTo {
	
	private String programName;
    private String depttName;
    private String learningAndOutcome;
    private String credit;
	public String getProgramName() {
		return programName;
	}
	public void setProgramName(String programName) {
		this.programName = programName;
	}
	public String getDepttName() {
		return depttName;
	}
	public void setDepttName(String depttName) {
		this.depttName = depttName;
	}
	public String getLearningAndOutcome() {
		return learningAndOutcome;
	}
	public void setLearningAndOutcome(String learningAndOutcome) {
		this.learningAndOutcome = learningAndOutcome;
	}
	public String getCredit() {
		return credit;
	}
	public void setCredit(String credit) {
		this.credit = credit;
	}

}
