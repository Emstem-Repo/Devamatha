package com.kp.cms.to.employee;

public class EmpGuideShipDetailsTO {
	private int id;
	private String phdScholarName;
	private String registrationYear;
	private String awarded;
	private String year;
	private String thesisName;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getPhdScholarName() {
		return phdScholarName;
	}
	public void setPhdScholarName(String phdScholarName) {
		this.phdScholarName = phdScholarName;
	}
	public String getRegistrationYear() {
		return registrationYear;
	}
	public void setRegistrationYear(String registrationYear) {
		this.registrationYear = registrationYear;
	}
	public String getAwarded() {
		return awarded;
	}
	public void setAwarded(String awarded) {
		this.awarded = awarded;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getThesisName() {
		return thesisName;
	}
	public void setThesisName(String thesisName) {
		this.thesisName = thesisName;
	}
}
