package com.kp.cms.to.employee;

public class EmpAwardTO {
	private int id;
	private String awardName;
	private String activityname;
	private String recognitionName;
	private String year;
	private String awardbodyName;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getAwardName() {
		return awardName;
	}
	public void setAwardName(String awardName) {
		this.awardName = awardName;
	}
	public String getActivityname() {
		return activityname;
	}
	public void setActivityname(String activityname) {
		this.activityname = activityname;
	}
	public String getRecognitionName() {
		return recognitionName;
	}
	public void setRecognitionName(String recognitionName) {
		this.recognitionName = recognitionName;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getAwardbodyName() {
		return awardbodyName;
	}
	public void setAwardbodyName(String awardbodyName) {
		this.awardbodyName = awardbodyName;
	}
}
