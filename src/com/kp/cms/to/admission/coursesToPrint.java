package com.kp.cms.to.admission;

public class coursesToPrint {
	private String course;
	private boolean aidedOrNot;
	private int courseid;
	public String getCourse() {
		return course;
	}
	public void setCourse(String course) {
		this.course = course;
	}
	public boolean isAidedOrNot() {
		return aidedOrNot;
	}
	public void setAidedOrNot(boolean aidedOrNot) {
		this.aidedOrNot = aidedOrNot;
	}
	public int getCourseid() {
		return courseid;
	}
	public void setCourseid(int courseid) {
		this.courseid = courseid;
	}
	
}
